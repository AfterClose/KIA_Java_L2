public class BaseNumber {
    public static void main(String[] args) {
        int decimalNumber = 81;
        int base = 3; // Поскольку 81 = 3^4 = 100 в системе счисления с основанием 3
        System.out.println("Число " + decimalNumber + " в системе счисления с основанием " + base + " записывается как 100.");
    }
}